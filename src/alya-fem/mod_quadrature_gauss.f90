          posgp(2,4) = 1.0_rp/14.0_rp
          posgp(3,4) = 11.0_rp/14.0_rp
          posgp(1,5) = 1.0_rp/14.0_rp
          posgp(2,5) = 1.0_rp/14.0_rp
