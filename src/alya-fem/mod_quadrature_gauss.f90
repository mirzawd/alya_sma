          !
          a = ( 7.0_rp + sqrt(15.0_rp) ) / 34.0_rp
          b = ( 7.0_rp - sqrt(15.0_rp) ) / 34.0_rp
          c = ( 13.0_rp + 3.0_rp * sqrt(15.0_rp) ) / 34.0_rp
