          posgp(:, 9) = (/ qp2rp(0.50000000000000000000_qp), qp2rp(0.50000000000000000000_qp),  qp2rp(0.00000000000000000000_qp) /)
          posgp(:,10) = (/ qp2rp(0.50000000000000000000_qp), qp2rp(0.00000000000000000000_qp),  qp2rp(0.50000000000000000000_qp) /)
