          posgp(:, 1) = (/ 0.25_rp , 0.25_rp , 0.25_rp /)
          posgp(:, 2) = (/ a, a, a/)
          posgp(:, 3) = (/ a, a, c/)
          posgp(:, 4) = (/ a, c, a/)
          posgp(:, 5) = (/ c, a, a/)
          posgp(:, 6) = (/ b, b, b/)
          posgp(:, 7) = (/ b, b, d/)
          posgp(:, 8) = (/ b, d, b/)
          posgp(:, 9) = (/ d, b, b/)
          posgp(:,10) = (/ e, e, f/)
          posgp(:,11) = (/ e, f, e/)
