       else if(ngaus==29) then

          !
          ! 6th order quadrature (exact for mass matrix), taken
          ! from Yu Jinyun: Symm. Gaussian quadrature formulae
          ! for tetrahedronal regions.
          !

          weigp(1)       = qp2rp(0.09040129046014750_qp)
          weigp(2:5)     = qp2rp(0.01911983427899124_qp)
