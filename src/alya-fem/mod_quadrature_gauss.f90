          weigp(10)  = d
          weigp(11)  = d

       else if(ngaus==14) then
          !
          ! 5th order 
          !
          weigp( 1)   = qp2rp(0.13283874668559071814_qp)
          weigp( 2)   = qp2rp(0.13283874668559071814_qp)
          weigp( 3)   = qp2rp(0.13283874668559071814_qp)
          weigp( 4)   = qp2rp(0.13283874668559071814_qp)
          weigp( 5)   = qp2rp(0.088589824742980710434_qp)
