          weigp( 4)   = h
          weigp( 5)   = h
          weigp( 6)   = i
          weigp( 7)   = i
          weigp( 8)   = i
          weigp( 9)   = i
          weigp(10)   = 5.0_rp / 567.0_rp 
          weigp(11)   = 5.0_rp / 567.0_rp 
          weigp(12)   = 5.0_rp / 567.0_rp 
          weigp(13)   = 5.0_rp / 567.0_rp 
          weigp(14)   = 5.0_rp / 567.0_rp 
