       posgl(6)=-0.7966664774136267395915539_rp
       posgl(7)= 0.9602898564975362316835609_rp
       posgl(8)=-0.9602898564975362316835609_rp
       weigl(1)= 0.362683783_rp
       weigl(2)= 0.362683783_rp
       weigl(3)= 0.313706646_rp
       weigl(4)= 0.313706646_rp
       weigl(5)= 0.222381034_rp
       weigl(6)= 0.222381034_rp
       weigl(7)= 0.101228536_rp
       weigl(8)= 0.101228536_rp
    else
       ierro=1
    end if

    if(ndime==2) then
       igaus=0
       do ilocs=1,nlocs
          do jlocs=1,nlocs
             igaus=igaus+1
             weigp(  igaus)=weigl(ilocs)*weigl(jlocs)
             posgp(1,igaus)=posgl(ilocs)
             posgp(2,igaus)=posgl(jlocs)
          end do
       end do

    else if(ndime==3) then
       igaus=0
       do ilocs=1,nlocs
          do jlocs=1,nlocs
             do klocs=1,nlocs
                igaus=igaus+1
                weigp(  igaus)=weigl(ilocs)*weigl(jlocs)*weigl(klocs)
                posgp(1,igaus)=posgl(ilocs)
                posgp(2,igaus)=posgl(jlocs)
                posgp(3,igaus)=posgl(klocs)
             end do
          end do
       end do
