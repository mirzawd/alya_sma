          posgp(:, 1) = (/ qp2rp(0.056881379520423421748_qp), qp2rp(0.31437287349319219275_qp), qp2rp(0.31437287349319219275_qp) /)
          posgp(:, 2) = (/ qp2rp(0.31437287349319219275_qp), qp2rp(0.056881379520423421748_qp), qp2rp(0.31437287349319219275_qp) /)
          posgp(:, 3) = (/ qp2rp(0.31437287349319219275_qp), qp2rp(0.31437287349319219275_qp),  qp2rp(0.056881379520423421748_qp) /)
