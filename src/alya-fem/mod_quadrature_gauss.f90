          posgp(:,5) = (/ qp2rp(0.67914317820120795168_qp),  qp2rp(0.10695227393293068277_qp), qp2rp(0.10695227393293068277_qp)  /)
          posgp(:,6) = (/ qp2rp(0.10695227393293068277_qp),  qp2rp(0.67914317820120795168_qp), qp2rp(0.10695227393293068277_qp)  /)
          posgp(:,7) = (/ qp2rp(0.10695227393293068277_qp),  qp2rp(0.10695227393293068277_qp), qp2rp(0.67914317820120795168_qp)  /)
          posgp(:,8) = (/ qp2rp(0.10695227393293068277_qp),  qp2rp(0.10695227393293068277_qp), qp2rp(0.10695227393293068277_qp)  /)

